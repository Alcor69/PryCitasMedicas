    private void cargarMedicos() {
        MedicoControlador controlador = MedicoControlador.getInstancia();

        for (MedicoModelo medico : controlador.listarMedicos()) {
            Object[] fila = {
                medico.getId(),
                medico.getNombres(),
                medico.getEspecialidad()
            };
            tableModel.addRow(fila);
        }
    }

